package com.example.adarshyadav.assignmentapplication.Pojo;

public class HolidayPojo {
    String HolidayOccasion;
    String HolidayDate;
    String HolidayDay;
    String CalenderCode;
    String CalenderName;
    String Year;

    public String getCalenderCode() {
        return CalenderCode;
    }

    public void setCalenderCode(String calenderCode) {
        CalenderCode = calenderCode;
    }

    public String getCalenderName() {
        return CalenderName;
    }

    public void setCalenderName(String calenderName) {
        CalenderName = calenderName;
    }

    public String getYear() {
        return Year;
    }

    public void setYear(String year) {
        Year = year;
    }

    public String getHolidayOccasion() {
        return HolidayOccasion;
    }

    public void setHolidayOccasion(String holidayOccasion) {
        HolidayOccasion = holidayOccasion;
    }

    public String getHolidayDate() {
        return HolidayDate;
    }

    public void setHolidayDate(String holidayDate) {
        HolidayDate = holidayDate;
    }

    public String getHolidayDay() {
        return HolidayDay;
    }

    public void setHolidayDay(String holidayDay) {
        HolidayDay = holidayDay;
    }
}
